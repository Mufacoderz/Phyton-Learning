a = 10
b = 3

#penjumlahan
hasil = a + b
print(a ,"+", b, " hasilnya = ", hasil)

#pengurangan
hasil = a - b
print(a ,"-", b, " hasilnya = ", hasil)

#perkalian
hasil = a * b
print(a ,"*", b, " hasilnya = ", hasil)

#pembagian
hasil = a / b
print(a ,":", b, " hasilnya = ", hasil)

#operasi eksponen/pangkat
hasil = a**b
print(a ,"pangkat", b, " hasilnya = ", hasil)

#operasi modulus/sisa bagi
hasil = a%b
print(a ,"sisa bagi", b, " hasilnya = ", hasil)

#operasi floor division /kebalikan modulus
hasil = a//b
print(a ,"//", b, " hasilnya = ", hasil)

